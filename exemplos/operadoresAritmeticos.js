console.log("10 + 2 = ", 10 + 2); // adição +
console.log("10 - 2 = ", 10 - 2); // subtração -
console.log("10 * 2 = ", 10 * 2); // multiplicação *
console.log("5 / 2 = ", 5 / 2); // divisão /
console.log("5 % 2 = ", 5 % 2); // resto(mod) %

// exponenciação **
console.log("10 ** 2 = ", 10 ** 2);
console.log("10 ** -2 = ", 10 ** -2);

let i = 5;
console.log("Incrementando 5", i++); // incremento ocorre depois da execução
i = 5;
console.log("Incrementando 5", ++i); // incremento ocorre antes da execução

i = 5;
console.log("Decrementando 5", i--); // decremento ocorre depois da execução
i = 5;
console.log("Decrementando 5", --i); // decremento ocorre antes da execução